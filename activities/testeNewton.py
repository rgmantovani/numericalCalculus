import numpy as np
from sympy import Symbol, log
from newton import newtonMethod, newtonSympy

#----------------------------------------------------------------
#----------------------------------------------------------------

# Teste: ln(x) + x - 4 = 0, es = 0.0001, x0 = 1.5

#----------------------------------------------------------------
#----------------------------------------------------------------

def fx(x):
  return(np.log(x) + x - 4)

def ffx(x): 
  return((1/x) + 1)

#----------------------------------------------------------------
#----------------------------------------------------------------

# testando metodo 1
print(newtonMethod(xo = 1.5, fx = fx, ffx = ffx, 
                   es = 1e-4, maxit = 1000))

#----------------------------------------------------------------
#----------------------------------------------------------------
# testando metodo 2

x = Symbol('x')
f = log(x) + x - 4
print(newtonSympy(xo = 1.5, f = f, es = 1e-4, maxit = 1000))

